#include "libft.h"
#include <stdlib.h>

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	while (*alst)
	{
		del((*alst)->next, 0);
		*alst = NULL;
	}
	del(*alst, 0);
	free(*alst);
}
